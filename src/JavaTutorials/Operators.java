package JavaTutorials;

public class Operators {
	
	public static void main(String args[]) {
		int x = 10;
		System.out.println(x); //10
		
		System.out.println(x++);//10 -- first displaying
		System.out.println(x);//11 -- after displaying it is incrementing
		
		System.out.println(++x);//12 --first incrementing
		System.out.println(x);//12 --displaying after incrementing
		
		System.out.println(x--);//12
		System.out.println(x);//11
		
		System.out.println(--x);//10
		System.out.println(x);//10
	}

}
 